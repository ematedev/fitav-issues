package it.ethica.esf.portlet.utility;

import java.util.Date;


public class CurriculumShooterInfo {
	
	private String matchDesc;
	private Date matchStartDate;
	private Date matchEndDate;
	private String spec;
	private String isIndividual;
	private String category;
	private String qualification;
	private int position;
	private int result;
	private int total;
	private int finalTotal;
	private int shootOff1;
	private int shootOff2;
	
	public String getMatchDesc() {
	
		return matchDesc;
	}
	
	public void setMatchDesc(String matchDesc) {
	
		this.matchDesc = matchDesc;
	}
	
	public Date getMatchStartDate() {
	
		return matchStartDate;
	}
	
	public void setMatchStartDate(Date matchStartDate) {
	
		this.matchStartDate = matchStartDate;
	}
	
	public Date getMatchEndDate() {
	
		return matchEndDate;
	}
	
	public void setMatchEndDate(Date matchEndDate) {
	
		this.matchEndDate = matchEndDate;
	}
	
	public String getSpec() {
	
		return spec;
	}
	
	public void setSpec(String spec) {
	
		this.spec = spec;
	}
	
	public String getIndividual() {
	
		return isIndividual;
	}
	
	public void setIndividual(String isIndividual) {
	
		this.isIndividual = isIndividual;
	}
	
	public String getCategory() {
	
		return category;
	}
	
	public void setCategory(String category) {
	
		this.category = category;
	}
	
	public String getQualification() {
	
		return qualification;
	}
	
	public void setQualification(String qualification) {
	
		this.qualification = qualification;
	}
	
	public int getPosition() {
	
		return position;
	}
	
	public void setPosition(int position) {
	
		this.position = position;
	}
	
	public int getResult() {
	
		return result;
	}
	
	public void setResult(int result) {
	
		this.result = result;
	}
	
	public int getTotal() {
	
		return total;
	}
	
	public void setTotal(int total) {
	
		this.total = total;
	}
	
	public int getFinalTotal() {
	
		return finalTotal;
	}
	
	public void setFinalTotal(int finalTotal) {
	
		this.finalTotal = finalTotal;
	}
	
	public int getShootOff1() {
	
		return shootOff1;
	}
	
	public void setShootOff1(int shootOff1) {
	
		this.shootOff1 = shootOff1;
	}
	
	public int getShootOff2() {
	
		return shootOff2;
	}
	
	public void setShootOff2(int shootOff2) {
	
		this.shootOff2 = shootOff2;
	}
	
}
